def load_movies(BUCKET, PATH):
    '''
    This function downloads the movies file from S3 and outputs it as a pandas dataframe.
    '''
    s3 = boto3.client('s3')

    # Creating the directory
    with tempfile.TemporaryDirectory() as td:

        td = Path(td)
        movies_path = td / 'movies.dat'

        # Downloading the file
        s3.download_file(BUCKET, PATH, str(movies_path))

        # Creating the dataframe
        movies = pd.read_csv(
            movies_path, sep='::', engine='python', names=['movie_id', 'title', 'genres'], encoding='latin-1'
        )

        movie_ids = movies["movie_id"].astype(int)
        id_to_row = {int(mid): i for i, mid in enumerate(movie_ids.tolist())}

    return movies, movie_ids, id_to_row


# Randomly selecting a user in the top 5%
def pick_top_user(df, seed=123):
    if df.empty:
        return None
    
    user_counts = df["user_id"].value_counts()
    if len(user_counts) == 0:
        return None

    # Computing 95th percentile cutoff
    cutoff = np.quantile(user_counts.values, 0.95)

    # Users in top 5%
    top_users = user_counts[user_counts >= cutoff].index.to_numpy()

    if len(top_users) == 0:
        return None

    rng = np.random.default_rng(seed)
    return int(rng.choice(top_users))

def format_movie_list(movie_ids, items_df):
    '''
    This function formats the movie list by joining the movie id, title, and genre
    '''

    m = items_df.set_index('movie_id')[['title', 'genres']]
    parts = []
    for mid in movie_ids:
        mid = int(mid)
        if mid in m.index:
            title = m.loc[mid, 'title']
            genres = m.loc[mid, 'genres']
            parts.append(f'{mid}: {title} [{genres}]')
        else:
            parts.append(str(mid))
    return ' | '.join(parts)

# Recommendation function for a general user

def recommend(user_id,
              events,
              E,
              item_ids,
              id_to_row,
              item_col='movie_id',
              ts_col='ts',
              topk=5,
              N=10,
              min_rating=4):
    '''
    This function creates a recommendation for the general user. It gets the N most recent events and select the ones with good rating.
    '''

    uev = (
        events[events['user_id'] == int(user_id)]
        .sort_values(ts_col, ascending=False)
        .tail(N)
    )
    uev = uev[uev['rating'] >= float(min_rating)]

    mids = [int(m) for m in uev[item_col].tolist() if int(m) in id_to_row]
    if not mids:
        return []

    rows = np.array([id_to_row[m] for m in mids], dtype=int)
    seen_rows = set(rows.tolist())

    # user profile = mean of watched embeddings
    u = E[rows].mean(axis=0)  # (d,)
    u = u / max(np.linalg.norm(u), 1e-12)

    # cosine similarity (since E is normalized)
    sims = E @ u  # (n_items,)

    # argsort desc, excluding seen
    candidates = [i for i in np.argsort(-sims) if i not in seen_rows]
    top_rows = candidates[:topk]

    return [int(item_ids[i]) for i in top_rows]


# Recommending to cold users based on popularity
def recommend_cold(events, items, k=5):
    '''
    This function creates a recommendation for cold users.
    '''

    # Select only the movie id's present on the item subset
    subset_ids = set(items['movie_id'].astype(int).tolist())

    # Creating recommendation is based on popularity
    pop = (events[events['movie_id'].isin(subset_ids)]
           ['movie_id'].value_counts().head(k).index.astype(int).tolist())
    return pop

