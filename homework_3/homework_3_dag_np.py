def run_recommend(**context):
  '''
  This function:
  - downloads embeddings from se
  - creates a recommendation for one cold and one top
  - creates a recommendations file
  '''

  s3 = boto3.client('s3')
  bucket_name = 'fingerson-lacombefarina-mwaa'

  # Downloading embeddings from S3
  with tempfile.TemporaryDirectory() as td:
    td = Path(td)
    emb_path = td / "full_embedding.npy"
    emb_key = "full_embedding.npy"
    s3.download_file(bucket_name, emb_key, str(emb_path))

    E = np.load(emb_path)
    norms = np.linalg.norm(E, axis=1, keepdims=True)
    E = E / np.maximum(norms, 1e-12)


#   # Grabbing data partition from download task
#   partition = xcom_to_df(context["ti"].xcom_pull(task_ids="download"))

  merged_key = context['ti'].xcom_pull(task_ids="download")
  with tempfile.TemporaryDirectory() as td:
    td = Path(td)
    local_path = td / "merged_partition.csv"
    s3.download_file(bucket_name, merged_key, str(local_path))

    # Load partition
    ratings = pd.read_csv(local_path)
    ratings['ts'] = pd.to_datetime(ratings['timestamp'], unit='s')

  # Downloading movies.dat from s3 and create item metadata
  movies, movie_ids, id_to_row = load_movies('fingerson-lacombefarina-mwaa', 'movies.dat')
  items = movies[['movie_id', 'title', 'genres']]

  # Picking top user
  # Pick top 5% user from entire dataset
  top_user = pick_top_user(ratings, seed=3)

  if top_user is None:
    print("No valid top user found.")
    return None

  print(f"Selected top user: {top_user}")

  # Creating the user counts and the number of interactions for it
  user_counts = ratings.groupby('user_id').size().rename('Num_Interactions')

  # Selecting the top 5% percentile
  top5_cutoff = int(np.quantile(user_counts.values, 0.95))

  # Extracting the last interaction time for the top user
  top_user_ratings = ratings[ratings['user_id'] == top_user]
  last_interaction_time = top_user_ratings['ts'].max()
  last_interaction_str = last_interaction_time.strftime("%Y-%m-%d %H:%M:%S")

  # Generating recommendations
  # Top user
  top_ids = recommend(
        user_id=top_user,
        events=ratings,
        E=E,
        item_ids=movie_ids,
        id_to_row=id_to_row,
        item_col='movie_id',
        ts_col='ts')

  top_user_row = {
        'User_Type': 'Top user',
        'User_ID': int(top_user),
        'Last_Interaction_Time': last_interaction_str,
        'Num_Interactions': int(user_counts.loc[top_user]),
        'Top5_Interaction_Cutoff': top5_cutoff,
        'Recommended_Movies': format_movie_list(top_ids, items),
    }

  # Cold user
  cold_ids = recommend_cold(ratings, items, k=5)

  cold_user_row = {
        'User_Type': 'Cold user',
        'User_ID': None,
        'Last_Interaction_Time': None,
        'Num_Interactions': 0,
        'Top5_Interaction_Cutoff': '',
        'Recommended_Movies': format_movie_list(cold_ids, items),
  }

  recs = pd.DataFrame([top_user_row, cold_user_row])

  return df_to_xcom(recs)


