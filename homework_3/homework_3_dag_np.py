def update_count(**context):
  '''
  This function:
  - uploads .json to s3 that just contains a number
  - at each run of the dag, it adds 1 to the number to keep track of the run
  '''
  s3 = boto3.client('s3')
  bucket_name = 'fingerson-lacombefarina-mwaa'
  run_count_key = 'dags/run_count.json'

  with tempfile.TemporaryDirectory() as td:
    td = Path(td)
    local_path = td/'run_count.json'

    s3.download_file(bucket_name, run_count_key, str(local_path))
    run_count = json.loads(local_path.read_text())
    run_count += 1 # increment
    local_path.write_text(json.dumps(run_count))

    s3.upload_file(str(local_path), bucket_name, run_count_key)

    print(f'Updated run_count to: {run_count}')

