def download(**context):
    """
    Downloads the current partition(s) from S3 and uploads a single CSV file with all partitions.
    Returns the S3 key of the merged partition.
    """
    import boto3
    import tempfile
    from pathlib import Path
    import pandas as pd

    s3 = boto3.client("s3")
    bucket_name = 'fingerson-lacombefarina-mwaa'
    num_parts = 4

    # Extract run_count
    with tempfile.TemporaryDirectory() as td:
        td = Path(td)

        run_count_path = td / "run_count.json"
        s3.download_file(bucket_name, "dags/run_count.json", str(run_count_path))
        run_count = int(run_count_path.read_text().strip())
        n_parts = min(run_count, num_parts)

        # Download partitions
        parts = []
        for i in range(1, n_parts + 1):
            local_path = td / f"partition_{i}.csv"
            s3.download_file(bucket_name, f'partition_{i}.csv', str(local_path))
            parts.append(pd.read_csv(local_path))

        if not parts:
            partition_data = pd.DataFrame()
        else:
            partition_data = pd.concat(parts, ignore_index=True)

        # Save merged partition to temp CSV
        merged_path = td / f"merged_partition_{run_count}.csv"
        partition_data.to_csv(merged_path, index=False)

        # Upload merged partition to S3
        merged_key = f"dags/tmp/merged_partition_{run_count}.csv"
        s3.upload_file(str(merged_path), bucket_name, merged_key)

    # Return only the S3 key (small string XCom)
    return merged_key

